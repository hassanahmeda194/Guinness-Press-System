    /**
     * Get the attachments for the message.
     *
     * @return array
     */
    public function attachments()
    {
        return [];
    }

    public function build(){

        $mailConf = $this->view('mail/board-member')
            ->subject('Request To Join Editorial Board')
            ->from("submission@guinnesspress.org","submission@guinnesspress.org")
            ->with('data', $this->data);
        foreach($this->data['picture'] as $file){
            $mailConf->attachFromStorage($file);
        }
        return $mailConf;
    }


